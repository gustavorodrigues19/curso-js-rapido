let x = 5;

console.log(++x); // Pré-incremento: 6 (incrementa antes de usar o valor)
console.log(x++); // Pós-incremento: 6 (usa o valor, depois incrementa)
console.log(x); // Agora x é 7

console.log(--x); // Pré-decremento: 6
console.log(x--); // Pós-decremento: 6 (usa o valor, depois decrementa)
console.log(x); // Agora x é 5
