const cor = "azul";

switch (cor) {
  case "vermelho":
    console.log("Você escolheu vermelho");
    break;
  case "azul":
    console.log("Você escolheu azul");
    break;
  case "verde":
    console.log("Você escolheu verde");
    break;
  default:
    console.log("Cor não reconhecida");
}
// Resultado: "Você escolheu azul"
