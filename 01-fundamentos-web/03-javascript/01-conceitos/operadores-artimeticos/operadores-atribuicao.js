let x = 10;

x += 5; // x = x + 5 → x = 15
console.log(x);

x -= 3; // x = x - 3 → x = 12
console.log(x);

x *= 2; // x = x * 2 → x = 24
console.log(x);

x /= 4; // x = x / 4 → x = 6
console.log(x);

x %= 2; // x = x % 2 → x = 0
console.log(x);

x = 3;
x **= 2; // x = x ** 2 → x = 9
console.log(x);
